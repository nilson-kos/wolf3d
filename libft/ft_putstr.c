#include "libft.h"
#include <unistd.h>

void	ft_putstr(char const *s)
{
	int		i;
	char	*c;

	c = (char *)(s);
	i = 0;
	if (c)
	{
		while (c[i] != '\0')
		{
			write(1, &c[i], 1);
			i++;
		}
	}
}
