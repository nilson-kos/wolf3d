#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int res;
	int neg;

	neg = 0;
	res = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-')
		neg = 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	if (neg == 1)
	{
		return (-res);
	}
	return (res);
}
